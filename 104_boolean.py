# Boolean and Comparison Operators

# Boolean
# It is a data type that is either True or False
print(True)
print(False)

print(type(True))
print(type(False))

# Comparison Operators
# Comparison operators allows to evaluate a code
# They output --> 'bool'

# asserting if something is equal
# use double equal to equate something
print(10 == '10')

num_a = 10
num_b = 7
# greater than

print('using >')
print(num_a > 10)
print(num_b > 10)

# smaller than

print('using <')
print(num_a < 10)
print(num_b < 10)

# greater or equal than

print('using >=')
print(num_a >= 10)
print(num_b >= 10)

# smaller or equal than

print('using <=')
print(num_a <= 10)
print(num_b <= 10)

# not equal
print('using !=')
print(10 != 13)

# is (of something)
print(0 is 0)