# Integers and Floats, Complex numbers, big ints
import math
# Integers
# Full numbers
print(10)
print(type(10))

# Floats
# Decimal numbers
print(10.0)
print(type(10.0))

# They can be used together
print(10/3)

# Add
print(3 + 4)

# Subtract
print(3 - 4)

# Divide
print(3 / 4)

# Multiply
print(3 * 4)

# Something with this % (modulus)
# This gives us the remainder
print(10 % 2)
print(10 % 3)

# Casting data types
# From string to integer
example_var = '10'
print(type(example_var))

example_var = int('10')
print(type(example_var))

# Useful function for ints
# Import math for some useful functions

random_num = 10.44323

# Rounding to a specific decimal

print(round(random_num, 2))