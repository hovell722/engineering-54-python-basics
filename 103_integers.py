# Integers and Floats, Complex numbers, big ints

# Integers
# Full numbers
print(10)
print(type(10))

# Floats
# Decimal numbers
print(10.0)
print(type(10.0))

# They can be used together
print(10/3)

# Add
print(3 + 4)

# Subtract
print(3 - 4)

# Divide
print(3 / 4)

# Multiply
print(3 * 4)

# Something with this % (modulus)
# This gives us the remainder
print(10 % 2)
print(10 % 3)

