# List and collection
# List work as we expect them, they keep stuff in a list

# Define a list using []
cringe_land_l = []

# how lists are organised
    # organised with index
    # numbers start at 0

# how to define a list
cringe_land_l = ['David', 'Teresa May', 'Boris', 'Mate Jeremy']
# index         [   0   ,       1      ,    2   ,       3     ]

# get one item from a list
print(cringe_land_l[1])

# get all item from a list
print(cringe_land_l)

# re assign item index 2 on list
cringe_land_l[2] = 'Ayman'
print(cringe_land_l)

# add one item to list
cringe_land_l.append('Joe')
print(cringe_land_l)

# add item in specific location on list
cringe_land_l.insert(1, 'Kevin')
print(cringe_land_l)


